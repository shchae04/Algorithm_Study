package study.boj.implementation;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

/**
 * BOJ 1193 - 분수찾기
 *
 * [1] 문제를 어떻게 봐야 하는가
 * 이 문제는 1번째, 2번째, 3번째 분수를 하나씩 세려고 하면 금방 헷갈린다.
 * 핵심은 "대각선 묶음"을 먼저 발견하는 것이다.
 *
 * 분수를 대각선 기준으로 적어 보면:
 *
 *                 1/1
 *              1/2   2/1
 *           3/1   2/2   1/3
 *        1/4   2/3   3/2   4/1
 *     5/1   4/2   3/3   2/4   1/5
 *
 * 각 줄이 하나의 "대각선"이다.
 *
 * 1번째 대각선: 1/1                                  -> 1개
 * 2번째 대각선: 1/2, 2/1                             -> 2개
 * 3번째 대각선: 3/1, 2/2, 1/3                        -> 3개
 * 4번째 대각선: 1/4, 2/3, 3/2, 4/1                   -> 4개
 * 5번째 대각선: 5/1, 4/2, 3/3, 2/4, 1/5              -> 5개
 *
 * 즉, n번째 대각선에는 분수가 n개 들어 있다.
 *
 * ------------------------------------------------------------
 * [2] 대각선의 방향 규칙
 *
 * 홀수 번째 대각선은
 * "아래로 갈수록 분자는 감소, 분모는 증가" 한다.
 *
 * 3번째 대각선: 3/1 -> 2/2 -> 1/3
 * 5번째 대각선: 5/1 -> 4/2 -> 3/3 -> 2/4 -> 1/5
 *
 * 짝수 번째 대각선은
 * "아래로 갈수록 분자는 증가, 분모는 감소" 한다.
 *
 * 2번째 대각선: 1/2 -> 2/1
 * 4번째 대각선: 1/4 -> 2/3 -> 3/2 -> 4/1
 *
 * 즉,
 * - 홀수 대각선: 위 -> 아래 로 갈수록 분자 감소 / 분모 증가
 * - 짝수 대각선: 위 -> 아래 로 갈수록 분자 증가 / 분모 감소
 *
 * ------------------------------------------------------------
 * [3] 문제 접근
 *
 * X번째 분수를 찾으려면, 먼저 X가 몇 번째 대각선에 속하는지 알아야 한다.
 *
 * 대각선별 개수 누적합은 이렇게 된다:
 * - 1번째 대각선까지: 1
 * - 2번째 대각선까지: 1 + 2 = 3
 * - 3번째 대각선까지: 1 + 2 + 3 = 6
 * - 4번째 대각선까지: 10
 * - 5번째 대각선까지: 15
 *
 * 예를 들어 X = 14 라면:
 * - 10보다는 크고
 * - 15 이하는 맞다
 * 따라서 14는 5번째 대각선에 있다.
 *
 * 그리고 5번째 대각선의 시작 번호는 11이므로
 * 14는 그 대각선에서 4번째 칸이다.
 *
 * 5번째 대각선은 홀수이므로:
 * 5/1, 4/2, 3/3, 2/4, 1/5
 * 여기서 4번째 칸은 2/4
 *
 * ------------------------------------------------------------
 * [4] 어떻게 최적화했는가
 *
 * 순진하게 풀면:
 * - 1번째 분수
 * - 2번째 분수
 * - 3번째 분수
 * ...
 * 이런 식으로 X번째까지 전부 만들어 가야 할 것 같지만,
 * 사실 그럴 필요가 없다.
 *
 * 우리는 "분수 하나하나"를 만들지 않고,
 * "대각선 단위"로 건너뛴다.
 *
 * 예를 들어 X = 1000이어도
 * 1개, 2개, 3개, 4개... 식으로 대각선 크기만 더해 가면서
 * 몇 번째 대각선인지 찾으면 된다.
 *
 * 즉, 핵심 최적화는:
 * - 모든 분수를 나열하지 않는다.
 * - X가 속한 대각선만 찾는다.
 * - 그 대각선 내부의 위치만 계산한다.
 *
 * 이 방식은 메모리를 거의 쓰지 않고,
 * 필요한 연산도 대각선 개수만큼만 수행한다.
 *
 * ------------------------------------------------------------
 * [5] 구현 순서 요약
 *
 * 1. lineNumber = 현재 대각선 번호
 * 2. countBeforeLine = 이전 대각선들까지의 누적 개수
 * 3. x가 현재 대각선 범위에 들어올 때까지 diagonal을 증가
 * 4. 현재 대각선에서 x가 몇 번째 칸인지 계산
 * 5. 대각선 번호가 홀수인지 짝수인지에 따라 분자/분모 계산
 */
public class boj1193 {
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int x = Integer.parseInt(br.readLine());

        // lineNumber: 현재 보고 있는 대각선 번호
        // countBeforeLine: 현재 대각선 바로 전까지 등장한 분수 개수의 합
        int lineNumber = 1;
        int countBeforeLine = 0;

        /*
         * x가 속한 대각선을 찾는 과정
         *
         * countBeforeLine + lineNumber 는
         * "현재 대각선까지 포함했을 때의 마지막 번호"를 뜻한다.
         *
         * 예)
         * lineNumber = 4, countBeforeLine = 6 이면
         * 4번째 대각선 범위는 7 ~ 10
         * 왜냐하면 이전까지 6개 있었고, 이번 대각선에 4개가 더 있으니
         * 끝 번호는 10이 되기 때문이다.
         *
         * x가 이 범위보다 크면 아직 뒤쪽 대각선에 있다는 뜻이므로
         * countBeforeLine에 현재 lineNumber를 더하고,
         * 다음 대각선으로 넘어간다.
         */
        while (x > countBeforeLine + lineNumber) {
            countBeforeLine += lineNumber;
            lineNumber++;
        }

        /*
         * 이제 x는 lineNumber번째 대각선에 속해 있다.
         *
         * orderInLine은 "현재 대각선에서 몇 번째 칸인지"를 뜻한다.
         *
         * 예를 들어 x = 14 라면
         * - 4번째 대각선까지의 누적합은 10
         * - 5번째 대각선 범위는 11 ~ 15
         * - 따라서 14는 5번째 대각선의 4번째 칸
         *
         * 즉, orderInLine = 14 - 10 = 4
         */
        int orderInLine = x - countBeforeLine;
        int topNumber;
        int bottomNumber;

        /*
         * 홀수 대각선과 짝수 대각선은 방향이 반대다.
         *
         * 홀수 대각선 그림:
         * 5/1
         * 4/2
         * 3/3
         * 2/4
         * 1/5
         *
         * 짝수 대각선 그림:
         * 1/4
         * 2/3
         * 3/2
         * 4/1
         *
         * 그래서 같은 orderInLine 이더라도
         * 홀수/짝수에 따라 분자와 분모 계산식이 서로 반대로 된다.
         */
        if (lineNumber % 2 == 1) {
            // 홀수 대각선
            // 분자: diagonal -> 1 로 감소
            // 분모: 1 -> diagonal 로 증가
            topNumber = lineNumber - orderInLine + 1;
            bottomNumber = orderInLine;
        } else {
            // 짝수 대각선
            // 분자: 1 -> diagonal 로 증가
            // 분모: diagonal -> 1 로 감소
            topNumber = orderInLine;
            bottomNumber = lineNumber - orderInLine + 1;
        }

        System.out.println(topNumber + "/" + bottomNumber);
    }
}
